          <h3> L’inventeur de la bicyclette moderne, celle que nous connaissons est l’oeuvre de Georges Juzan
          un mécanicien Bordelais. Malgré cette invention, il n’eut pas le temps de le breveter et mourut à 63ans.
          Néanmoins merci à lui!  <br>
          </h3>
          <figure> <img src="bordeaux-1.gif" alt="L'inventeur de bicyclette vient de Bordeaux" /> </figure>
          <h2> <strong> Le Coca de Bordeaux </strong> </h2>
          <h3> Comme vous savez, la marque Coca-Cola a été une invention USA… FAUX ! Il est vrai que le créateur est Américain,
          John S Pemberton, a crée cette célèbre boisson mais il s’est inspiré d’un breuvage médicinal Français:
          Feuilles de Coca et noix de Kola mélangés à du vin Bordelais ! On s’est fait voler le succès. Hop hop hop !!
          On a entendu que ça parlait de Coca.. Aller vient par içi <a href="http://www.10infossur.fr/article/coca-cola/10infossur-coca-cola.php">
