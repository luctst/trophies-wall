          <h2> <strong> Les entrailles de la Garonne </strong> </h2>
          <h3> La « merveilleuse » Garonne borde notre cher Bordeaux. Avec son eau si vive et trouble,
          elle nous laisse apercevoir des rondins de bois, des branches mais une fois à marée basse
          c’est un tout autre spectacle: des épaves Allemandes apparaissent. Ceci dure depuis 60ans et sont
          des vestiges de la seconde guerre mondiale. Un vestige en plus.  <br>
          </h3>
          <figure> <img src="bordeaux-3.gif" alt="La garonne détient bien des secrets"/> </figure>
          <h2> <strong> Sortez couverts !   </strong> </h2>
          <h3> En notre noble région Bordelaise, trouver de la neige en saison hivernale est bien rare.
          Pourtant le 21 Février 1956, Bordeaux, se réveilla un matin avec une énorme couche de neige
          recouvrant toute la ville ! Un station de ski à domicile !<br>
          </h3>
          <figure> <img src="bordeaux-4.gif" alt="Bordeaux sous la neige en 1956"/> </figure>
          <h2> <strong> Bon appétit bien-sûr !   </strong> </h2>
          <h3> Il faut savoir qu’il existe un Kebab plus que gardé ! Effectivement entre les bars de la Victoire
          et les boites de Paludate, il y a le Adana Kebab dans une ancienne banque et plus précisément dans
          une chambre forte. Si vous avez l’occasion d’y manger,
          vous pourrez dire que vous avez trouvé un trésor…  <br>
          </h3>
          <figure> <img src="bordeaux-5.gif" alt="Il existe un kebab dans une ancienne banque à Bordeaux"/> </figure>
          <h2> <strong> Le pont Napoléon ?? </strong> </h2>
          <h3> Le fameux pont de pierres Bordelais a été construit suite à la demande de Napoléon en 1830
          et celui-ci compte 17 arches or c’est le même nombre de Napoléon… Coïncidence ?
          Vous avez 4 heures !   <br>
          </h3>
          <h2> <strong> L’abus d’alcool est dangereux pour la santé ! </strong> </h2>
          <h3> Née en 1714, Marie Brizard, dévouée aux nécessiteux, reçut d’ « un voyageur venu des îles »
          la recette de l’anisette. Dans un premier temps, elle l’utilise pour réconforter les malades anémiés.
          Puis à l’âge de 40 ans, elle fonde avec son neveu une société de liqueur… Qui commercialise aujourd’hui
          230 millions de bouteilles dans le monde: Merci Marie Brizard.  <br>
          </h3>
          <figure> <img src="bordeaux-7.gif" alt="Marie brizard fondatrice de la vodka qui à la base était un médicament"/> </figure>
          <h2> <strong> Une Utopie enrichie:   </strong> </h2>
          <h3> Réputée pour ses films indépendants et ses produits bio, l’Utopia, église Saint-Siméon jadis,
          fut également une salpêtrière ainsi qu'une école navale. Fait moins connu des Bordelais,
          une fabrique de boîtes de conserve a également élu domicile entre ses murs.
          De cette activité est né un ustensile indispensable : l'ouvre-boîte !
          On dit merci qui ? Merci Bordeaux !<br> 
          </h3>
          <h2> <strong> Un peu d'histoire </strong> </h2>
          <h3> Si la place changea plusieurs fois de nom et de rôle, elle porte aujourd’hui le nom des religieux Capucins.
          Plus tard, sous la révolution, la Place des Capucins devint la place des droits de l’homme,
          pour finalement accueillir à partir de 1857 le marché aux bœufs de la ville.
          Ce sont ces pavillons métalliques venus tout droit de Paris, qui abritèrent le marché des Capucins,
          et ce jusqu’en 1957. Ils furent finalement remplacés par le béton que l’on connait aujourd’hui.
          Une place qui a tant à raconter.  <br>
          </h3>
          <h2> <strong> Encore de l'histoire, si si il le faut </strong> </h2>
          <h3> La Révolution vit la création du parti des Girondins, mené par Vergniaud.
          Les Girondins prônaient une république de type fédéral et s'opposaient aux Jacobins.
          Accusés par les Montagnards de conspirer contre la république, 22 Girondins furent
          exécutés après une nuit de beuverie à la Conciergerie. <br> 
          </h3>
