        <h3> Cyril est resté jusqu’à l’âge de 30ans chez ses parents,
        un Tanguy plus communément appelé aujourd’hui. Il avait du mal à trouver du travail,
        mais c’est en 2004 où il quitta la maison familiale grâce à la présentation du Morning
        Live qu’il faisait. Ses parents ont toujours été là pour le soutenir et il avoua:
        « Si j’ai pu faire de la télé, et réaliser ce que j’avais en tête,
        c’est vraiment grâce à eux. » Si si la famille !!<br> 
